package stringexample;

public class Charatexample 
{

	public static void main(String[] args) 
	{
		String str = "This is charat example program ";
		char c1 = str.charAt(0);  
		char c2 = str.charAt(8);
		char c3 = str.charAt(10);
		System.out.println("Character at index 0 = "+c1);
		System.out.println("Character at index 8 = "+c2);
		System.out.println("Character at index 10 ="+c3);
	}

}
